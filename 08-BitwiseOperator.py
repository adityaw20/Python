# Bitwise Operator

p = 5 #(binary 0101)
q = 3 #(binary 0011)
print("p & q:",p & q) # AND ->0001->1
print("p | q:",p | q) # OR ->0111 ->7
print("p ^ q:",p ^ q) # XOR ->0110 ->6
print("~p:",~p) #NOT -> -(p+1) ->6
print("p <<1 :",p << 1) #Left shift -> 1010 ->10
print("p >> 1 :",p>>1)  # Right shift ->0010 ->2
