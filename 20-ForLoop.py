# for loop 
# the for loop is to iterate over a sequence (list , tuple ,string , range )

# Example :loop thruogh a list 
fruits = ["Apple", "Banana", "Cherry "]
for fruit in fruits:
    print(fruit)

#Example : Using range() in for loop 
for i in range(5):  # range(5)-> 0,1,2,3,4,
    print(i)

# Example : range( start , stop , step )
for i in range(2,10,2): # 2,4 6 8
    print(i)